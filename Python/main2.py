a = input( 'Вам нравится кофе? (True или False): ')
b = input( 'Вам нравится чай? (True или False): ')

print ('Вы предпочитаете и кофе и чай: ', a and b )
print ('Вы предпочитаете или кофе или чай: ', a or b )
print ('Вы предпочитаете не кофе: ', not a )
print ('Вы предпочитаете не чай: ', not b )
print ('Вы предпочитаете одинаково и кофе и чай: ', a == b )
print ('Вы предпочитаете по-разному кофе и чай: ', a != b)